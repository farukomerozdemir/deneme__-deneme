package day11_while_Dowhilee;

public class c02while_Loop {
    public static void main(String[] args) {
        //1 den 10a sayıları yazıdrın
        for (int i = 1; i <=10 ; i++) {
            System.out.print(i+" ");
        }//while de loop da veriable,bitiş şartı-artış mikartı yoktur
        System.out.println("");
        int sayi=1;
        while (sayi<=10){
            System.out.print(sayi+" ");
            sayi+=2;

        }

    }
}
